            <article class="conseil">
                <h3>Conseils</h3>
                <img src="https://www.nantesmetropole.fr/medias/photo/mozaic_1439900356479.png" alt="petit bonhomme qui utilise une poubelle" class="poubelle">
                <div class="conseil-text">
                    <p>Localiser les déchèteries et consulter leurs horaires, connaître les jours de ramassage des poubelles, devenir un as du tri sélectif et du compostage… N’en jetez plus ; dans cette rubrique : toute l’info service pour gérer au mieux vos sacs et vos bacs.</p>
                    <ul>
                        <li><strong>Les déchets ménagers :</strong>
                            <br>Il s’agit des déchets non dangereux et non recyclables qui rentrent facilement dans votre bac. Ils ne sont pas issus d’activités spécifiques (ex : un matériau ou un objet issus de la déconstruction, de la mécanique…).
                            <br>Ces déchets sont collectées en porte à porte avec quelques exceptions telles que le quartier de Trentemoult où ont été implantées des colonnes enterrées. Les déchets ménagers sont collectés en bacs pour des raisons d’hygiène et de sécurité des agents de collecte. La collecte s’effectue 1 à 2 fois selon les communes ou quartiers de Nantes Métropole.</li>
                        <li><strong>Les déchets recyclables :</strong>
                            <br>Il s’agit des bouteilles et flacons en plastique, des papiers, des cartons et briques alimentaires, des emballages métalliques : annuaire, baril de lessive, barquette aluminium, boîte de conserve, boîte à oeufs en carton, bombe aérosol, bombe mousse à raser, bouteille d'huile plastique, bouteille en plastique, bouteille produit ménager vide, brique alimentaire, cahier, canette, carton, enveloppe, flacon produit ménager vide, flacon en plastique, journaux, livre, magazine, revue, papier, sac en papier...
                            <br>Ils sont collectés en bacs ou en sacs. Certaines bennes sont équipées de deux compartiments distincts, ce qui permet de collecter en même temps les ordures ménagères et les déchets recyclables sans les mélanger (ils sont vidés sur deux tas différents au centre de tri).</li>
                        <li><strong>Les déchets dangereux des ménages</strong>
                            <br>Ces déchets, produits en petites quantités par les ménages, présentent un risque pour l’environnement et les personnes (toxiques, inflammables…) et sont essentiellement des : peintures, vernis, colles, tubes fluorescents, diluants, solvants, acides, bases, radiographies, piles, aérosols, produits d’entretien…</li>
                        <li>Les encombrants
                            <br>Il s’agit des ordures ménagères qui ne rentrent pas dans le bac ou sont en quantités importantes.
                        </li>
                    </ul>
